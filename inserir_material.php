    <span style="font-size: 14px; font-weight: 700;">O que são frações?<br></span>
    <span style="font-size: 14px;">Frações são formas de representar partes de um todo. Quando dividimos algo em partes iguais, usamos frações para mostrar quantas dessas partes estamos considerando.<br><br></span>
    <span style="font-size: 14px; font-weight: 700;">Por exemplo</span>
    <span style="font-size: 14px;">, se uma pizza foi dividida em 4 pedaços iguais e você comeu 1, dizemos que você comeu 1/4 da pizza.<br>
    Uma fração tem duas partes:<br>
    O número de cima (numerador) mostra quantas partes estamos usando.<br>
    O número de baixo (denominador) mostra em quantas partes o todo foi dividido.</span>
